#include "push_swap.h"

t_list	*ft_lstnew(int content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (new == NULL)
		ft_error(1);
	new->content = content;
	new->index = 0;
	new->prev = NULL;
	new->next = NULL;
	return (new);
}
